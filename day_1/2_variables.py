# 1. variable is a name that refers to a value

a = 10
b = 20
c = "Hello"
float = 3.14
boolean = True

# integer variable
print("the value of a is",a)  # Output: 10
print("type of number is",type(a))  # Output: <class 'int'>

# string variable
print("the value of string is",c)  # Output: Hello
print("type of string is",type(c))  # Output: <class 'str'>

# float variable
print("the value of float is",float)  # Output: 3.14
print("type of float is",type(float))  # Output: <class 'float'

# boolean variable
print("the value of boolean is",boolean)  # Output: True
print("type of boolean is",type(boolean))  # Output: <class 'bool'>

